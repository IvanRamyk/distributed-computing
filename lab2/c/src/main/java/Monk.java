public class Monk {
    String monastery;
    int power;

    public Monk(String monastery, int power) {
        this.monastery = monastery;
        this.power = power;
    }

    public String getMonastery() {
        return monastery;
    }

    public void setMonastery(String monastery) {
        this.monastery = monastery;
    }

    public int getPower() {
        return power;
    }

    public void setPower(int power) {
        this.power = power;
    }
}
